type MascotProps = Omit<
  ComponentPropsWithoutRef<"img">,
  "alt" | "className" | "height" | "sizes" | "src" | "srcSet" | "width"
> &
  VariantProps<typeof mascotVariants> & {
    alt?: string;
    className?: string;
    decorative?: boolean;
    imageClassName?: string;
    name: MascotName;
    sizes?: string;
    variant?: MascotVariant;
  };

export function Mascot({
  alt,
  className,
  decoding = "async",
  decorative = true,
  imageClassName,
  loading = "lazy",
  name,
  size,
  sizes,
  variant = "mark",
  ...props
}: MascotProps) {
  const asset = mascotAssets[name][variant];
  const resolvedSize = size ?? "md";
  const resolvedAlt = decorative ? "" : (alt ?? asset.defaultAlt);

  return (
    <picture className={cn(mascotVariants({ size: resolvedSize }), className)}>
      <source
        srcSet={asset.webpSrcSet}
        sizes={sizes ?? sizeToSizes[resolvedSize]}
        type="image/webp"
      />
      <img
        alt={resolvedAlt}
        aria-hidden={decorative ? true : undefined}
        className={cn("block h-auto w-full", imageClassName)}
        decoding={decoding}
        height={asset.height}
        loading={loading}
        sizes={sizes ?? sizeToSizes[resolvedSize]}
        src={asset.pngFallback}
        srcSet={asset.pngSrcSet}
        width={asset.width}
        {...props}
      />
    </picture>
  );
}

export type { MascotName, MascotProps, MascotSize, MascotVariant };
